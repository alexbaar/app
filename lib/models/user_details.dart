class UserDetails {
  final String name;
  final String surname;
  final String username;
  final String email;
  final String dob;
  final String phoneNumber;
  final String imagePath;

  UserDetails({
    required this.name,
    required this.surname,
    required this.username,
    required this.email,
    required this.dob,
    required this.phoneNumber,
    required this.imagePath,
  });
}
